#include <unistd.h>

void	ft_putchar(void)
{
	write(1, ",", 1);
	write(1, " ", 1);
}

void	ft_print_comb(void)
{
	int	l1;
	int	l2;
	int	l3;

	l1 = 48;
	while (l1 <= 55)
	{
		l2 = l1 + 1;
		while (l2 <= 56)
		{
			l3 = l2 + 1;
			while (l3 <= 57)
			{
				write(1, &l1, 1);
				write(1, &l2, 1);
				write(1, &l3, 1);
				if (l1 != 55 || l2 != 56 || l3 != 57)
					ft_putchar();
				l3++;
			}
			l2++;
		}
		l1++;
	}
}
